import numpy as np
def euler_method(func,x0,y0,h,x_end):
    x_values = [x0]
    y_values = [y0]
    x = x0
    y = y0
    while x < x_end:
        y = y + h*func(x,y)
        x = x + h
        x_values.append(x)
        y_values.append(y)
    return x_values,y_values

def my_func(x,y):
    return x + y

x0 = 0
y0 = 1
h = 0.1
x_end  = 2

x_values, y_values = euler_method(my_func,x0,y0,h,x_end)
print("x values:-",x_values)
print("y values:-",y_values)



