// write program area of rectangle
#include<iostream>
using namespace std;
int main(){
    int l,w,area;
    cout<<"Enter the length:-"<<endl;
    cin>>l;
    cout<<"Enter the width:-"<<endl;
    cin>>w;
    area = l * w;
    cout<<"Area of rectangle "<<l<<" * "<<w<<" = "<<area;
    return 0;
}
